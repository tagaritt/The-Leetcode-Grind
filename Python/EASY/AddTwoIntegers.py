class Solution(object):
  def sum(self, num1, num2):
    """
    TASK:
    Given two integers 'num1' and 'num2', return the sum of the two integers.

    VARIABLE TYPES:
    :type num1:  int
    :type num2:  int
    :rtype:      int

    EXAMPLE 1:
      Input:       num1 = 12, num2 = 5
      Output:      17
      Explanation: num1 is 12, num2 is 5, and their sum is 12 + 5 = 17, so 17 is returned.

    EXAMPLE 2:
      Input:       num1 = -10, num2 = 4
      Output:      -6
      Explanation: num1 + num2 = -6, so -6 is returned.
    
    IMPLEMENTATION PROPOSAL:
    Create a simple solution within the return statement where the two input variables are added directly and the sum is the only thing returned.
    No need to overcomplicate, just complete the task.
    """

    return num1 + num2
